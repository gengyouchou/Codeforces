#define ab(x) (((x) < 0) ? -(x) : (x))

int dfs(int x, int y, const int tx, const int ty)
{
    if (ty - y < 0 || tx - x > ty - y)
    {
        return -1;
    }

    return ty - y + ab((ty - y) - (tx - x));
}

void solve()
{
    int x1, y1, x2, y2;
    cin >> x1 >> y1 >> x2 >> y2;

    int ans = dfs(x1, y1, x2, y2);

    cout << ans;
}

