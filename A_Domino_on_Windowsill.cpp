using ll = long long;
using ld = long double;

const ll N = 1'000'000'000'000L;

#define ab(x) (((x) < 0) ? -(x) : (x))

bool solve()
{
    int n, k1, k2;
    cin >> n >> k1 >> k2;

    int nw, nb;
    cin >> nw >> nb;

    int w = (ab(k1 - k2)) / 2 + min(k1, k2);

    int b1 = n - k1, b2 = n - k2;

    int b = (ab(b1 - b2)) / 2 + min(b1, b2);

    if (w >= nw && b >= nb)
    {
        return true;
    }
    else
    {
        return false;
    }
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    cout.tie(nullptr);

    int t = 0;

    cin >> t;

    for (int i = 0; i < t; ++i)
    {

        if (solve())
        {
            cout << "YES";
        }
        else
        {
            cout << "NO";
        }

        cout << endl;
    }
}