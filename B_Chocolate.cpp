void solve()
{
    int n;
    cin >> n;
    vector<int> vec(n);
    for (auto &x : vec)
    {
        cin >> x;
    }

    if (accumulate(vec.begin(), vec.end(), 0ll) == 0)
    {
        cout << 0;
        return;
    }

    int j = 0;

    ll ans = 1;

    for (int i = 0; i < n;)
    {
        j = i + 1;

        while (j < n && vec[j] == 0)
        {
            ++j;
        }

        if (i < n && j < n && vec[j] == 1 && vec[i] == 1 && j - i > 1)
        {
            ans *= (j - i);
        }

        i = j;
    }

    cout << ans;
}

