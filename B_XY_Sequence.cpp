void solve()
{
    int n, b, x, y;
    cin >> n >> b >> x >> y;

    long long sum = 0;

    long long pre = 0;

    for (int i = 0; i < n; ++i)
    {
        if (pre + x > b)
        {
            pre = pre - y;
        }
        else
        {
            pre = pre + x;
        }

        sum += pre;
    }

    cout << sum;
}

