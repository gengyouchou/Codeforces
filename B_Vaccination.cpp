void solve()
{
    int n, k, d, w;
    cin >> n >> k >> d >> w;
    vector<int> times(n);
    for (auto &x : times)
    {
        cin >> x;
    }

    int now = 0;
    int count = 0;

    while (now < n)
    {
        ++count;

        ll openTime = times[now] + w;
        int pack = k;

        while (now < n && pack > 0 && times[now] <= openTime + d)
        {
            --pack;
            ++now;
        }
    }

    cout << count;
}

