void solve()
{
    int n;
    cin >> n;
    vector<int> vec(n);
    for (auto &x : vec)
    {
        cin >> x;
    }

    sort(vec.begin(), vec.end());

    int j = n - 1, i = j - 1;

    int count = 0;

    while (i >= 0)
    {
        if (vec[j] > vec[i])
        {
            --j;
            --i;
            ++count;
        }
        else
        {
            --i;
        }
    }

    cout << count;
}

