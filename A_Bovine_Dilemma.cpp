void solve()
{
    int n;
    cin >> n;
    vector<int> vec(n);
    for (auto &x : vec)
    {
        cin >> x;
    }

    unordered_set<int> se;

    for (int len = 2; len <= n; ++len)
    {

        for (int i = 0; i < n - len + 1; ++i)
        {
            int j = i + len - 1;

            int base = vec[j] - vec[i];

            se.insert(base);
        }
    }

    cout << se.size();
}

